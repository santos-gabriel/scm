    private void carregarRegistros() {
        DefaultTableModel modelo = (DefaultTableModel) tblCidades.getModel();
        modelo.setNumRows(0);
        CtrlCidade.PesquisarTodos().forEach((c) -> {
            modelo.addRow(new Object []{
                c.getCodCidade(),
                c.getDescricao()
            });
        });
    }

